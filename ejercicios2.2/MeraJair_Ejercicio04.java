public class MeraJair_Ejercicio04 {
    public static void main(String[] args) {
        int a = 7;
        int b = 4;
        int c = 9;
        while (a != c) {
            if (c > 9) {
                b += 5;
            } else {
                a += 2;
            }
        }
        System.out.println("a = " + a + " b = " + b + " c = " + c);
    }
}
